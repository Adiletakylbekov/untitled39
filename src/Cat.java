public class Cat {
    String name;
    int age;
    String color;
    double weight;


    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }
    String getname(){
      return name;
    }

    public int getAge() {
        return age;
    }
    String getColor(){
        return color;
    }
    double getweight(){
        return weight;
    }
}
